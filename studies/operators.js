// Arithmetic operators— Arithmetic Operators includes operators like Addition, Subtraction, Multiplication, Division and Modulus. The numbers (in an arithmetic operation) are called operands. The operation (to be performed between the two operands) is defined by an operator.
// +	Addition	10 + 2 = 12
// –	Subtraction	10 – 2 = 8
// *	Multiplication	10 * 2 = 20
// /	Division	10 / 2 = 5
// %	Modulus – It returns the remainder after the division	10 % 2 = 0 (Here remainder is zero). If it is 10 % 3 then it will be 1.
// Comparison operators-- Comparison operators are used in logical statements to determine equality or difference between variables or values. Comparison and Logical operators are used to test for true or false.
// Example: 

// Operator	Description
// ==		equal to
// ===		equal value and equal type
// !=		not equal
// !==		not equal value or not equal type
// >		greater than
// <		less than
// >=		greater than or equal to
// <=		less than or equal to
// ?		ternary operator

